Seorang petugas ingin menghitung probabilitas untuk
mendapatkan 4 bola lampu yang rusak dari suatu
sampel acak sebanyak 20 bola lampu, apabila
diketahui bahwa 10 % dari bola lampu tersebut
rusak. Nilai probabilitas ini dapat diperoleh dari tabel
binomial yang dibuat berdasarkan distribusi
binomial

dbinom(x=4, size=20, prob=0.1)

